package com.gsjp.apps;

public class Arrayexample1 {

	public static void main(String[] args) {
		int [] arraytest ;
		arraytest = new int[6];
		
		arraytest[0] = 1;
		arraytest[1] = 2;
		arraytest[2] = 4;
		arraytest[3] = 5;
		arraytest[4] = 7;
		arraytest[5] = 10;
		
System.out.println("Value at index 0: " + arraytest[0]);
System.out.println("Value at index 1: " + arraytest[1]);
System.out.println("Value at index 2: " + arraytest[2]);
System.out.println("Value at index 3: " + arraytest[3]);
System.out.println("Value at index 4: " + arraytest[4]);
System.out.println("Value at index 5: " + arraytest[5]);
	}

}
